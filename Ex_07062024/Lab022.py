val = None
# data type - None
print(val)
# Nothing
# None is non a default value
# None is not 0
# None is not an empty string
# None is not the same as False

name = ""  # empty string - memory allocated
name = None
print(id(name))  #73823
print(name)  # None
print(type(name))  # None type
name = 'Amit'
print(id(name))  # 153876
print(name)  # Amit
