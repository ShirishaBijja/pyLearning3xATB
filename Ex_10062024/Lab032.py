# Ternary operator

pramod_marks = 90
amit_marks = 97
# x > y -> do something -
# y > x -> so something else
print("pramod is winner" if pramod_marks > amit_marks else "amit is the winner")
# ternary operator is an if-else loop in one line

# if - else
if pramod_marks > amit_marks:
    print("pramod is the winner")
else:
    print("amit is the winner")


